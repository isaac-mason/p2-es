import { ContactMaterial } from '../../src/material/ContactMaterial'
import { Material } from '../../src/material/Material'

describe('ContactMaterial', () => {
    test('construct', () => {
        const m1 = new Material(),
            m2 = new Material()
        new ContactMaterial(m1, m2)

        // Should throw error if not passed materials
        expect(function () {
            // @ts-expect-error not providing constructor args
            new ContactMaterial()
        }).toThrow()

        // Should get new ids
        expect(new ContactMaterial(m1, m2).id).not.toBe(new ContactMaterial(m1, m2).id)
    })
})
